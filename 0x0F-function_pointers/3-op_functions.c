
/**
* op_add - func returns the addition of a and b.
* @a: int a
* @b: int b
* Return: int result
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
* op_sub - func returns the difference of a and b.
* @a: int a
* @b: int b
* Return: int
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
* op_mul - func returns the product of a and b.
* @a: int a
* @b: int b
* Return: int
*/

int op_mul(int a, int b)
{
	return (a * b);
}


/**
* op_div - func returns the division of a and b.
* @a: int a
* @b: int b
* Return: int
*/

int op_div(int a, int b)
{
	return (a / b);
}


/**
* op_mod - func returns the modular of a and b.
* @a: int a
* @b: int b
* Return: int
*/

int op_mod(int a, int b)
{
	return (a % b);
}
